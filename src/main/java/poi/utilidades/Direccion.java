package poi.utilidades;

public class Direccion {
	private String calle;
	private int numero;
	private int piso;
	private String departamento;
	private String unidad;
	private int codigoPostal;
	private String entreCalleDerecha;
	private String entreCalleIzquierda;
	private String barrio;
	private String localidad;
	private String pais;
	private String provincia;
	
	public String getCalle() {
		return calle;
	}
	public void setCalle(String calle) {
		this.calle = calle;
	}
	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero = numero;
	}
	public int getPiso() {
		return piso;
	}
	public void setPiso(int piso) {
		this.piso = piso;
	}
	public String getDepartamento() {
		return departamento;
	}
	public void setDepartamento(String departamento) {
		this.departamento = departamento;
	}
	public String getUnidad() {
		return unidad;
	}
	public void setUnidad(String unidad) {
		this.unidad = unidad;
	}
	public int getCodigoPostal() {
		return codigoPostal;
	}
	public void setCodigoPostal(int codigoPosta) {
		this.codigoPostal = codigoPosta;
	}
	public String getEntreCalleDerecha() {
		return entreCalleDerecha;
	}
	public void setEntreCalleDerecha(String entreCalleDerecha) {
		this.entreCalleDerecha = entreCalleDerecha;
	}
	public String getEntreCalleIzquierda() {
		return entreCalleIzquierda;
	}
	public void setEntreCalleIzquierda(String entreCalleIzquierda) {
		this.entreCalleIzquierda = entreCalleIzquierda;
	}
	public String getBarrio() {
		return barrio;
	}
	public void setBarrio(String barrio) {
		this.barrio = barrio;
	}
	public String getLocalidad() {
		return localidad;
	}
	public void setLocalidad(String localidad) {
		this.localidad = localidad;
	}
	public String getPais() {
		return pais;
	}
	public void setPais(String pais) {
		this.pais = pais;
	}
	public String getProvincia() {
		return provincia;
	}
	public void setProvincia(String provincia) {
		this.provincia = provincia;
	}
	
	
	
}
